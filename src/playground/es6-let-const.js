// "var" datatype
// can Overriding variable using "var" datatype
var nameVar = "Punitkumar";
console.log("name: ", nameVar);
nameVar = "like";
console.log("Name: ", nameVar);

// "let" datatype
// reassign but cannot redefine(duplicate)
let nameLet = "Pompae";
console.log("US defence: ", nameLet);
nameLet = "Trump";
console.log("US defence: ", nameLet);

// "const" datatype
// cannot redefine and reassign
const nameConst = "Frank";
// nameConst = "mike";
console.log("Name: ", nameConst);

